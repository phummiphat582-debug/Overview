const CACHE = "atv-cache-v2"; // ðŸ‘ˆ à¹€à¸›à¸¥à¸µà¹ˆà¸¢à¸™à¹€à¸§à¸­à¸£à¹Œà¸Šà¸±à¸™à¸—à¸¸à¸à¸„à¸£à¸±à¹‰à¸‡à¸—à¸µà¹ˆà¸­à¸±à¸žà¹€à¸”à¸•

self.addEventListener("install", e => {
  self.skipWaiting(); // à¸šà¸±à¸‡à¸„à¸±à¸š activate à¸—à¸±à¸™à¸—à¸µ
  e.waitUntil(
    caches.open(CACHE).then(cache =>
      cache.addAll([
        "./",
        "./index.html",
        "./manifest.json"
      ])
    )
  );
});

self.addEventListener("activate", e => {
  e.waitUntil(
    caches.keys().then(keys =>
      Promise.all(
        keys.filter(k => k !== CACHE).map(k => caches.delete(k))
      )
    )
  );
  self.clients.claim();
});

self.addEventListener("fetch", e => {
  e.respondWith(
    caches.match(e.request).then(res => res || fetch(e.request))
  );
});
